function [F y]=fitness(x)
%[xv fv]=myGA(@fitness,0,30,50,10000,0.75,0.3,0.001)

% F = x^3-60*x^2+900*x+100;
values = [ 8,9,15,6,16,9,1,4,14,9,3,7,12,4,15,5,18,5,15,4,6,2,12,14,11,9,13,13,14,13,19,4 ];
volume = [ 11,22,12,21,21,13,1,10,13,8,6,25,13,27,12,23,12,24,23,11,6,24,28,10,20,13,25,23,5,26,30,15 ];
weight = [ 22,15,4,5,10,19,21,20,8,13,2,3,3,17,12,5,12,4,1,21,14,23,17,15,20,22,25,0,22,15,25,13 ];
v_sum = 1000;
w_sum = 1000;

while v_sum>75||w_sum>80
    v_sum = 0;
    w_sum = 0;
    F = 0;
    for i=1:length(x)
        if(x(i))
            v_sum = v_sum+volume(i);
            w_sum = w_sum+weight(i);
            F = F+values(i);
        end
        
    end   
    
    if v_sum>75||w_sum>80
        y = rand(length(x),1);
        y(y>0.75)=1;
        y(y<=0.75)=0;
        x=y;
    end
   
end
F=F;
y= x;





